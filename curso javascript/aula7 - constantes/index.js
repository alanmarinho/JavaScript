// As constantes são variáveis que não podem ter seu valor alterado
// as constantes sempre tem que ser iniciadas com um valor

const nome = 'João';
console.log(nome);

const idade = 25;
// O javaScript é um linguagem tipada, então todas as variáveis e constantes tem o seu tipo.
// Para saber qual o tipo de uma variavel basta usar o comando typeof()

console.log(typeof nome);
console.log(typeof idade);